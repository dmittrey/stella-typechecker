#!/bin/bash

# Проверяем, что передан аргумент
if [ -z "$1" ]; then
    echo "Использование: $0 <директория>"
    exit 1
fi

DIR="$1"

# Создаём директорию (вместе с родительскими)
mkdir -p "$DIR"

# Создаём файлы 1.txt ... 10.txt
for i in $(seq 1 10); do
    touch "$DIR/$i.txt"
done

echo "Директория '$DIR' создана и заполнена файлами 1.txt ... 10.txt"
